package BasiscofJava;

public class Switchcase {

	public static void main(String[] args) {
		int age=18;
		switch (age) {
		case 19:
			System.out.println("ur age is not 18");
			
		case 18:
			System.out.println("ur age is not correct");
			//break;
			
		default:
			System.out.println("u r not in the correct age");
			break;
		}

	}

}
